package day18_loops;

public class BackwardsInterviewQuestion {
    public static void main(String[] args) {

        //starts from 20 and go back to 1

        for (int i = 0; i <=20 ; i++) {
            System.out.println(i);                      //starts from 0 to go 20

        }

        System.out.println("---------------");

        for (int i = 20; i >=0; i--) {                   //starts from 20 to go 0 so .minus.
            System.out.println(i);

        }





    }
}
