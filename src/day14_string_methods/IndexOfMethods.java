package day14_string_methods;

public class IndexOfMethods {
    public static void main(String[] args) {
        String str = "loop";
        //            0123

        System.out.println(str.indexOf("l"));   // 0
        System.out.println(str.indexOf("o"));   // 1

        System.out.println(str.indexOf('l'));               //index char

        System.out.println(str.indexOf("op"));


        System.out.println(str.indexOf("oo"));                //index String


















    }
}
